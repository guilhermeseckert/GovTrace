/**
 * Returns a human-readable country name for an ISO 3166-1 alpha-2 code.
 * Falls back to the raw code string if unmapped.
 */
export const getCountryName = (code: string): string =>
  COUNTRY_NAMES[code.toUpperCase()] ?? code

/**
 * Converts a 2-letter ISO country code to a Unicode flag emoji using regional indicator symbols.
 * Example: "CA" -> "🇨🇦"
 */
export const getFlag = (code: string): string => {
  const upper = code.toUpperCase()
  if (upper.length !== 2) return ''
  const base = 0x1f1e6 - 65 // 'A'.charCodeAt(0) = 65
  return String.fromCodePoint(base + upper.charCodeAt(0), base + upper.charCodeAt(1))
}

