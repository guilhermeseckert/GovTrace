/**
 * Maps a 5-digit DAC sector code to a broad thematic label.
 * First tries exact lookup, then falls back to 3-digit prefix grouping.
 * Returns the raw code as a last resort.
 */
export const getSectorTheme = (code: string): string => {
  if (!code) return 'Unknown'
  const exact = DAC_SECTORS[code]
  if (exact) return exact
  const prefix = code.slice(0, 3)
  const theme = SECTOR_PREFIX_THEMES[prefix]
  if (theme) return theme
  return code
}
